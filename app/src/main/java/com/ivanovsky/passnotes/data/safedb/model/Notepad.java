package com.ivanovsky.passnotes.data.safedb.model;

public class Notepad {

	private String uid;
	private String title;

	public Notepad() {
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
}
